    //Hide all sub menu panels
    private void hideSubMenus() {
        for (JPanel sub : subMenus) {
            if (sub.isVisible()) {
                sub.setVisible(false);;
            }
        }
    }

    //Show specified sub menu panel
    private void showSubMenu(JPanel subMenu, boolean subSub) {
        if (!subMenu.isVisible() && !subSub) {
            hideSubMenus();
            subMenu.setVisible(true);
        } else if (!subMenu.isVisible() && subSub) {
            subMenu.setVisible(true);
        } else {
            subMenu.setVisible(false);
        }
    }

